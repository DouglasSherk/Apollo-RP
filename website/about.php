    <div class="title">About Apollo RP</div>
    <p>Deep Purple RP was started in August, 2006 by Doug Sherk after deciding 
    that the various limitations of HarbuWare RP had to be corrected. As such, 
    he started a new plugin set with the intention of providing an alternative 
    to servers in production. It is now known as Apollo RP. Apollo RP has 3 main 
    goals:</p>
    <ul>
      <li>To provide an out-of-the-box usable plugin set for anyone to use for 
      their server regardless of scripting/server administration abilities.</li>
      <li>To provide a fast, effective and easy-to-use means of establishing a 
      roleplay environment.</li>
      <li>To allow easy expansion by developers for new experiences.</li>
